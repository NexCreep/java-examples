/*
 * La abstraccion de una clase permite que cuando una clase la herede
 * deba de redefinir sus metodos abstractos. 
 * 
 * Las clases abstractas no se pueden inicializar
 * 
 * Los metodos abstractos:
 *      - Al menos un método abstracto
 *      - Métodos abtractos unicamente en clase abstracta
 *      - Los métodos abstractos no tienen cuerpo
 *      - Pueden contener método no abstracto
 * 
*/
public abstract class Abstraccion
{
    public abstract void print(String msg);
    
    
}
